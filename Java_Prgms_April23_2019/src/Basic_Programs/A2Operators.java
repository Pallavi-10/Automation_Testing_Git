package Basic_Programs;

public class A2Operators {

	public static void main(String[] args) 
	{

int i =10;
int j =20;

System.out.println(i+j);//Sum
System.out.println(j-i);//Minus
System.out.println(i/j);//quotient 
System.out.println(i*j);//Multiply
System.out.println(i-j);//Minus(negative)
System.out.println(i%j);//Reminder

System.out.println(i>j);// Boolean
System.out.println(j>i);

	}

}
