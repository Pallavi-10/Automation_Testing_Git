package Basic_Programs;

public class J4EvenOdd_Notusingoperator
{

	public static void main(String[] args) 
	{
		
		int n = 59;
		int res = n;
		
		while(res>=2)
		{
			res=res-2;
			
		}
		if(res==1)
		{
			System.out.println("odd");
		}
		else
		{
			System.out.println("Even");
		}

	}

}
